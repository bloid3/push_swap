#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	if (!new)
		return ;
	aux = ft_lstlast(*lst);
	if (!aux)
		*lst = new;
	else
		aux->next = new;
}
